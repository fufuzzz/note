
import math

# 数学功能
#   sum() 求和
print(sum([1, 2, 3, 4]))
print(sum(range(1, 101)))

#   max() : 最大数
#   min() : 最小数
print(max([1, 20, 3]))
print(min([1, 20, 3]))


#   pow() : 次方  333
print(pow(2, 3), 2**3, math.pow(2, 3))


#   abs() : 绝对值
print(abs(-3))  # 3

#   round() : 四舍五入
print(round(3.4567))  # 3
print(round(3.4567, 2))  # 3.46

# 数学函数
print(math.sqrt(81))  # 9
print(math.ceil(3.1))  # 向上取整 4
print(math.floor(3.1))  # 向下取整 3
print(math.pi)  # π 3.1415926
